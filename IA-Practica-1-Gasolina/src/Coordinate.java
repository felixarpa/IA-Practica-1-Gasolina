public class Coordinate {
    private int CoordX;
    private int CoordY;

    public Coordinate(int coordX, int coordY) {
        CoordX = coordX;
        CoordY = coordY;
    }

    public int getCoordX() {
        return CoordX;
    }

    public void setCoordX(int coordX) {
        CoordX = coordX;
    }

    public int getCoordY() {
        return CoordY;
    }

    public void setCoordY(int coordY) {
        CoordY = coordY;
    }
}
